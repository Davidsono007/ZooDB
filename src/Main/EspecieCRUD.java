    // Função usada para puxar os dados do Banco de Dados para a tabela, como um SELECT * FROM
     public void readJTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) lst_Especie.getModel();
        modelo.setNumRows(0);
        EspecieDAO edao = new EspecieDAO();
        
        for(Especie e: edao.readEspecie()) {
            
            modelo.addRow(new Object[]{
                e.getId_especie(),
                e.getNm_especie(),
                e.getDs_dieta()
            });
            
        }
    
}
       
     // Usado ao buscar um ID 
     public void searchId(String id) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) lst_Especie.getModel();
        modelo.setNumRows(0);
        EspecieDAO edao = new EspecieDAO();
        
        for(Especie e: edao.searchEspecieId(id)) {
            
            modelo.addRow(new Object[]{
                e.getId_especie(),
                e.getNm_especie(),
                e.getDs_dieta()
            });
            
        }
    
}
