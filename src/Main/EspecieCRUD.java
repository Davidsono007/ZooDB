    // Cria um novo dado
    private void btn_CriarEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CriarEspecieActionPerformed
        Especie e = new Especie();
        EspecieDAO dao = new EspecieDAO();
        
        e.setNm_especie(txt_NomeEspecie.getText());
        e.setDs_dieta(txt_Dieta.getText());
        dao.createEspecie(e);
        
        txt_NomeEspecie.setText("");
        txt_Dieta.setText("");
        
        try {
            readJTable();
        } catch (SQLException ex) {
            Logger.getLogger(EspecieCRUD.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_CriarEspecieActionPerformed

    // Atualiza um dado
    private void btn_AtualizarEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarEspecieActionPerformed
        
        if (lst_Especie.getSelectedRow() != -1) {
            
            Especie e = new Especie();
            EspecieDAO dao = new EspecieDAO();

            e.setId_especie((int)lst_Especie.getValueAt(lst_Especie.getSelectedRow(), 0));
            e.setNm_especie(txt_NomeEspecie.getText());
            e.setDs_dieta(txt_Dieta.getText());
            dao.updateEspecie(e);

            txt_NomeEspecie.setText("");
            txt_Dieta.setText("");

            try {
                readJTable();
            } catch (SQLException ex) {
                Logger.getLogger(EspecieCRUD.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um item da lista com um clique do mouse.");
        }
        
    }//GEN-LAST:event_btn_AtualizarEspecieActionPerformed

