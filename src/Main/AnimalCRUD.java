    // Função usada para puxar os dados do Banco de Dados para a tabela, como um SELECT * FROM
     public void readJTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) lst_Animal.getModel();
        modelo.setNumRows(0);
        AnimalDAO adao = new AnimalDAO();
        
        for(Animal a: adao.readAnimal()) {
            
            modelo.addRow(new Object[]{
                a.getId_animal(),
                a.getNm_animal(),
                a.getDs_sexo(),
                a.getId_especie(),
                a.getId_cercado()
            });
            
        }
    
}
     
