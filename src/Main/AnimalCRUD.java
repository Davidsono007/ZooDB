    private void lst_AnimalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_AnimalMouseClicked
        
        if(lst_Animal.getSelectedRow() != -1) {
            
            txt_AnimalNome.setText(lst_Animal.getValueAt(lst_Animal.getSelectedRow(), 1).toString());
            txt_AnimalSexo.setText(lst_Animal.getValueAt(lst_Animal.getSelectedRow(), 2).toString());
            txt_AnimalEspecie.setText(lst_Animal.getValueAt(lst_Animal.getSelectedRow(), 3).toString());
            txt_AnimalCercado.setText(lst_Animal.getValueAt(lst_Animal.getSelectedRow(), 4).toString());
        }
    }//GEN-LAST:event_lst_AnimalMouseClicked

