    // Atualiza um dado
    private void btn_AtualizarAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarAnimalActionPerformed
        if (lst_Animal.getSelectedRow() != -1) {
            
            Animal a = new Animal();
            AnimalDAO dao = new AnimalDAO();

            a.setId_animal((int)lst_Animal.getValueAt(lst_Animal.getSelectedRow(), 0));
            a.setNm_animal(txt_AnimalNome.getText());
            a.setDs_sexo(txt_AnimalSexo.getText());
            a.setId_especie(Integer.parseInt(txt_AnimalEspecie.getText()));
            a.setId_cercado(Integer.parseInt(txt_AnimalCercado.getText()));
            dao.updateAnimal(a);

            txt_AnimalNome.setText("");
            txt_AnimalSexo.setText("");
            txt_AnimalEspecie.setText("");
            txt_AnimalCercado.setText("");

            try {
                readJTable();
            } catch (SQLException ex) {
                Logger.getLogger(EspecieCRUD.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um item da lista com um clique do mouse.");
        }
    }//GEN-LAST:event_btn_AtualizarAnimalActionPerformed

