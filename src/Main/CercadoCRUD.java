    // Função usada para puxar os dados do Banco de Dados para a tabela, como um SELECT * FROM
     public void readJTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) lst_Cercado.getModel();
        modelo.setNumRows(0);
        CercadoDAO cdao = new CercadoDAO();
        
        for(Cercado c: cdao.readCercado()) {
            
            modelo.addRow(new Object[]{
                c.getId_cercado(),
                c.getNm_cercado(),
                c.getDs_tipo(),
            });
            
        }
    
}
     // Usado ao buscar um ID
     public void searchId(String id) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) lst_Cercado.getModel();
        modelo.setNumRows(0);
        CercadoDAO cdao = new CercadoDAO();
        
        for(Cercado c: cdao.searchCercadoID(id)) {
            
            modelo.addRow(new Object[]{
                c.getId_cercado(),
                c.getNm_cercado(),
                c.getDs_tipo(),
            });
            
        }
    
}
     
