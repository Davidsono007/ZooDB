    // Deleta um dado
    private void btn_DeletarCercadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarCercadoActionPerformed
       if (lst_Cercado.getSelectedRow() != -1) {
            
            Cercado c = new Cercado();
            CercadoDAO dao = new CercadoDAO();

            c.setId_cercado((int)lst_Cercado.getValueAt(lst_Cercado.getSelectedRow(), 0));
            dao.updateCercado(c);

            txt_CercadoNome.setText("");
            txt_CercadoTipo.setText("");

            try {
                readJTable();
            } catch (SQLException ex) {
                Logger.getLogger(EspecieCRUD.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um item da lista com um clique do mouse.");
        }
    }//GEN-LAST:event_btn_DeletarCercadoActionPerformed

